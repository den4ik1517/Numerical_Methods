def gaussian_elimination(A, b, x0):
  """
  Обчислює рішення системи лінійних рівнянь методом Гауса з вибором головного елемента по стовпцю, з початковим наближенням.

  Аргументи:
    A: Матриця системи рівнянь.
    b: Вектор вільних членів.
    x0: Вектор початкових наближень.

  Повертає:
    x: Вектор рішень.
  """

  n = len(A)

  for i in range(n):
    # Знаходимо головний елемент у стовпці i.
    k = i
    for j in range(i + 1, n):
      if abs(A[j][i]) > abs(A[k][i]):
        k = j

    # Обмінюємо рядки i та k.
    A[i], A[k] = A[k], A[i]
    b[i], b[k] = b[k], b[i]

    # Отримуємо верхню трикутну матрицю.
    for j in range(i + 1, n):
      A[j][i] /= A[i][i]
      for k in range(i + 1, n):
        A[j][k] -= A[j][i] * A[i][k]

  # Знаходимо рішення системи.
  x = x0
  for i in range(n - 1, -1, -1):
    x[i] = (b[i] - sum(A[i][j] * x[j] for j in range(i + 1, n))) / A[i][i]

  return x

A = [[0.4, 0.3], [1.4, -0.5]]
b = [0.1, 0.1]
x0 = [0.1, 0.1]

x = gaussian_elimination(A, b, x0)

print(x)
